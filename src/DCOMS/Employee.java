    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed

          try {             
            int row = OrderApprove.getSelectedRow();
            String value = OrderApprove.getModel().getValueAt(row,0).toString();
                       
            LoginInterface object = (LoginInterface)Naming.lookup("rmi://localhost:1044/sub");
            object.approveOrder(value);
             
            JOptionPane.showMessageDialog(null,"Order is approved");
             
            DefaultTableModel table = (DefaultTableModel)OrderApprove.getModel();
            table.setRowCount(0);
            display();
                      
        } catch (Exception e) {}        
    }//GEN-LAST:event_approveBtnActionPerformed

