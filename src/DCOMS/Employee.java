    public void display() throws RemoteException{

            ArrayList<Order> list = orderList(Login.username);        
            DefaultTableModel table = (DefaultTableModel)OrderApprove.getModel();

            Object[] row = new Object [6];
            for( int i=0;i<list.size();i++) {
                row[0] = list.get(i).getOrderid();
                row[1] = list.get(i).getItemid();
                row[2] = list.get(i).getItemname();
                row[3] = list.get(i).getCustomerid();
                row[4] = list.get(i).getQuantity();
                row[5] = list.get(i).getTotal();
                table.addRow(row); 
            } 
        }

    public ArrayList<Order> orderList(String username) throws RemoteException {

        LoginInterface object = null;
        String condition = "STATUS IS NULL";
        
        try {
            object = (LoginInterface)Naming.lookup("rmi://localhost:1044/sub");
        } catch (NotBoundException | RemoteException| MalformedURLException e) {e.printStackTrace();}

        return object.getOrderList(condition);
    }
    
